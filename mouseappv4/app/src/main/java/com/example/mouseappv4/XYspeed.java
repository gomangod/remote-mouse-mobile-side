package com.example.mouseappv4;

public class XYspeed {
    public XYspeed(int xspeed, int yspeed){
        this.speedy =yspeed;
        this.speedx=xspeed;
    }
    public int speedx ;
    public int speedy;



    public int getSpeedy() {
        return speedy;
    }

    public void setSpeedy(int speedy) {
        this.speedy = speedy;
    }

    public int getXspeed() {
        return speedx;
    }

    public void setXspeed(int xspeed) {
        this.speedx = xspeed;
    }
}
